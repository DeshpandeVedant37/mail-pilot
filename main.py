# ─────────────────────────────────────────────

def extract_slots_from_text(text: str) -> dict:
    now = datetime.now(tz=IST)
    now_str = now.strftime("%Y-%m-%d %H:%M %Z")

    try:
        resp = requests.post(
            "https://api.groq.com/openai/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {os.getenv('GROQ_API_KEY')}",
                "Content-Type": "application/json"
            },
            json={
                "model": "llama-3.3-70b-versatile",
                "messages": [
                    {
                        "role": "system",
                        "content": (
                            f"Current time is {now_str}. "
                            "Extract the meeting time from the email text. "
                            "Reply with ONLY a JSON object like this: "
                            "{\"start\": \"2026-04-05T16:00:00+05:30\", \"end\": \"2026-04-05T17:00:00+05:30\"}. "
                            "Use IST timezone (+05:30). Duration is 1 hour unless specified. "
                            "If no specific time mentioned, use tomorrow at 10am IST. "
                            "Do not include any explanation, only the JSON."
                        )
                    },
                    {
                        "role": "user",
                        "content": text
                    }
                ],
                "temperature": 0
            },
            timeout=10
        ).json()

        content = resp["choices"][0]["message"]["content"].strip()
        content = content.replace("```json", "").replace("```", "").strip()
        times = json.loads(content)
        print(f"[TIME EXTRACT] start={times['start']} end={times['end']}")
        return {
            "time_min": times["start"],
            "time_max": times["end"]
        }

    except Exception as e:
        print(f"[TIME EXTRACT ERROR] {e}")
        fallback = now.replace(hour=10, minute=0, second=0, microsecond=0) + timedelta(days=1)
        return {
            "time_min": fallback.isoformat(),
            "time_max": (fallback + timedelta(hours=8)).isoformat()
        }


# ─────────────────────────────────────────────
# ATTENDEE EXTRACTION
# ─────────────────────────────────────────────

def extract_attendees(email: dict) -> list:
    attendees = set()
    sender = email.get("sender", "")
    if "@" in sender:
        match = re.search(r'<(.+?)>', sender)
        attendees.add(match.group(1) if match else sender.strip())
    found = re.findall(r'[\w.+-]+@[\w-]+\.[a-zA-Z]{2,}', email.get("body", ""))
    attendees.update(found)
    return list(attendees)


# ─────────────────────────────────────────────
