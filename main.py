# ─────────────────────────────────────────────

def find_slots(attendees, time_min, time_max, duration=60):
    try:
        resp = requests.post(
            f"{CALENDAR_SERVICE_URL}/calendar/find-slots",
            json={
                "attendee_emails": attendees,
                "time_min": time_min,
                "time_max": time_max,
                "duration_minutes": duration,
                "max_slots": 3,
            },
            timeout=10
        )
        resp.raise_for_status()
        return resp.json().get("available_slots", [])
    except Exception as e:
        print(f"[ERROR] find-slots failed: {e}")
        log_activity("find-slots failed", str(e), "error")
        return []


def create_event(summary, slot, attendees, description=""):
    try:
        resp = requests.post(
            f"{CALENDAR_SERVICE_URL}/calendar/create-event",
            json={
                "summary": summary,
                "start_dt": slot["start"],
                "end_dt": slot["end"],
                "attendee_emails": attendees,
                "description": description,
                "timezone_str": "Asia/Kolkata",
                "send_updates": "all",
            },
            timeout=10
        )
        resp.raise_for_status()
        return resp.json()
    except Exception as e:
        print(f"[ERROR] create-event failed: {e}")
        log_activity("create-event failed", str(e), "error")
        return None


