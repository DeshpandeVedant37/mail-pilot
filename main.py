def build_schedule_reply(slots, event_result, attendees):
    if event_result and event_result.get("status") == "created":
        ist = ZoneInfo("Asia/Kolkata")
        start_dt = datetime.fromisoformat(slots[0]["start"]).astimezone(ist)
        end_dt = start_dt + timedelta(hours=1)
        date_str = start_dt.strftime("%A, %d %B %Y")
        start_str = start_dt.strftime("%I:%M %p")
        end_str = end_dt.strftime("%I:%M %p")
        attendee_list = "\n".join([f"  • {a}" for a in attendees])
        tz_lines = format_time_for_attendees(slots[0], attendees)
        return (
            f"Hi,\n\n"
            f"Your meeting has been successfully scheduled!\n\n"
            f"━━━━━━━━━━━━━━━━━━━━━━━━\n"
            f"  MEETING CONFIRMATION\n"
            f"━━━━━━━━━━━━━━━━━━━━━━━━\n"
            f"  Date     : {date_str}\n"
            f"  Time     : {start_str} – {end_str} IST\n"
            f"  Duration : 1 hour\n"
            f"  Attendees:\n{attendee_list}\n\n"
            f"  Local times:\n{tz_lines}\n"
            f"━━━━━━━━━━━━━━━━━━━━━━━━\n\n"
            f"A Google Calendar invite has been sent to all attendees.\n\n"
            f"Thanks,\nMail Pilot Assistant"
            + AI_DISCLAIMER
        )
    elif event_result and event_result.get("status") == "duplicate":
        existing_start = event_result.get("start", "")
        if existing_start:
            ist = ZoneInfo("Asia/Kolkata")
            existing_dt = datetime.fromisoformat(existing_start).astimezone(ist)
            existing_str = existing_dt.strftime("%A, %d %B %Y at %I:%M %p IST")
            return (
                f"Hi,\n\nYou already have a meeting scheduled at {existing_str}.\n\n"
                "This time slot is taken! Could you please suggest a different time "
                "and I'll schedule it for you?\n\nThanks!"
                + AI_DISCLAIMER
            )
        return (
            "Hi,\n\nThis time slot is already booked! "
            "Could you please suggest a different time?\n\nThanks!"
            + AI_DISCLAIMER
        )
    elif slots:
        ist = ZoneInfo("Asia/Kolkata")
        slot_options = "\n".join([
            f"• {datetime.fromisoformat(s['start']).astimezone(ist).strftime('%A, %d %B %Y at %I:%M %p IST')}"
            for s in slots
        ])
        return (
            f"Hi,\n\nI found these available time slots:\n\n{slot_options}\n\n"
            "Please reply with your preferred slot and I'll confirm the meeting."
            + AI_DISCLAIMER
        )
    else:
        return (
            "Hi,\n\nI wasn't able to find a common available slot in the requested window. "
            "Could you suggest a few alternative times?\n\nThanks!"
            + AI_DISCLAIMER
        )


def build_update_reply(thread_messages):
    recent = thread_messages[-3:] if len(thread_messages) > 3 else thread_messages
    summary_lines = [
        f"• {msg.get('sender', 'Unknown')}: {msg.get('snippet', '')[:200]}"
        for msg in recent
    ]
    return (
        "Hi,\n\nHere's a quick summary of this thread:\n\n"
        + "\n".join(summary_lines)
        + "\n\nLet me know if you need more details!"
        + AI_DISCLAIMER
    )


