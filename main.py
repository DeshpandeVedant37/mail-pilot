def process_email(service, email):
    print(f"\nFrom: {email['sender']}")
    print(f"Subject: {email['subject']}")

    log_activity("Email received", f"From {email['sender']} — {email['subject']}", "info")

    category = classify_email(email["subject"], email["body"])
    print(f"Category: {category}")

    if category == "SCHEDULE":
        handle_schedule(service, email)
    elif category == "UPDATE":
        handle_update(service, email)
    else:
        handle_other(service, email)

    try:
        mark_as_read(service, email["id"])
    except Exception as e:
        print(f"Could not mark as read: {e}")

    save_processed_id(email["id"])


