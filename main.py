# POLLING LOOP
# ─────────────────────────────────────────────

def polling_loop(interval_seconds=20):
    service = get_gmail_service()
    print("Mail Pilot started. Polling every 20 seconds...")
    while True:
        try:
            processed_ids = load_processed_ids()
            emails = get_unread_emails(service)
            new_emails = [e for e in emails if e["id"] not in processed_ids]
            if new_emails:
                print(f"\nFound {len(new_emails)} new email(s)")
                for email in new_emails:
                    process_email(service, email)
            else:
                print("No new emails.")
        except Exception as e:
            print(f"Error: {e}")
        time.sleep(interval_seconds)


if __name__ == "__main__":
    polling_loop()