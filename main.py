# TIMEZONE HELPERS
# ─────────────────────────────────────────────

def get_timezone_for_email(email: str) -> str:
    domain_tz = {
        "gmail.com": "Asia/Kolkata",
        "yahoo.com": "America/New_York",
        "outlook.com": "Europe/London",
        "hotmail.com": "Europe/London",
    }
    domain = email.split("@")[-1].lower()
    return domain_tz.get(domain, "Asia/Kolkata")


def format_time_for_attendees(slot: dict, attendees: list) -> str:
    lines = []
    seen_tz = set()
    for email in attendees:
        tz_str = get_timezone_for_email(email)
        if tz_str in seen_tz:
            continue
        seen_tz.add(tz_str)
        tz = ZoneInfo(tz_str)
        local_dt = datetime.fromisoformat(slot["start"]).astimezone(tz)
        lines.append(
            f"  • {email}: {local_dt.strftime('%I:%M %p')} ({tz_str.split('/')[-1].replace('_', ' ')})"
        )
    return "\n".join(lines)


