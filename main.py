def handle_schedule(service, email):
    print(f"[SCHEDULE] {email['subject']}")
    print(f"[SCHEDULE] BODY: {email['body'][:200]}")
    attendees = extract_attendees(email)
    print(f"[SCHEDULE] Attendees: {attendees}")

    # Check if time is clear enough to proceed
    try:
        clarity_resp = requests.post(
            "https://api.groq.com/openai/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {os.getenv('GROQ_API_KEY')}",
                "Content-Type": "application/json"
            },
            json={
                "model": "llama-3.3-70b-versatile",
                "messages": [
                    {
                        "role": "system",
                        "content": (
                            "You are an assistant that checks if a scheduling email contains enough information to book a meeting. "
                            "Reply with ONLY a JSON object like this: "
                            "{\"clear\": true} if the email contains a specific date and time, OR "
                            "{\"clear\": false, \"question\": \"<a single follow-up question to ask the sender>\"} if the time is vague or missing. "
                            "Examples of unclear: 'sometime next week', 'in the morning', 'soon', 'at your convenience', no time mentioned at all. "
                            "Examples of clear: 'tomorrow at 3pm', 'Monday April 7th at 10am', 'this Friday at 2:30pm IST'. "
                            "Only reply with the JSON, nothing else."
                        )
                    },
                    {
                        "role": "user",
                        "content": email["body"][:500]
                    }
                ],
                "temperature": 0,
                "max_tokens": 100
            },
            timeout=10
        ).json()

        clarity_content = clarity_resp["choices"][0]["message"]["content"].strip()
        clarity_content = clarity_content.replace("```json", "").replace("```", "").strip()
        match = re.search(r'\{.*\}', clarity_content, re.DOTALL)
        clarity = json.loads(match.group()) if match else {"clear": True}

    except Exception as e:
        print(f"[SCHEDULE] Clarity check failed: {e}")
        clarity = {"clear": True}

    # If time is unclear, ask follow-up and stop
    if not clarity.get("clear", True):
        question = clarity.get("question", "Could you please provide a specific date and time for the meeting?")
        reply = (
            f"Hi,\n\n"
            f"Thanks for reaching out! I'd love to help schedule this meeting.\n\n"
            f"{question}\n\n"
            f"Once you confirm, I'll get it booked right away.\n\n"
            f"Thanks,\nMail Pilot Assistant"
            + AI_DISCLAIMER
        )
        send_reply(service, email, reply)
        log_activity("Follow-up sent", f"Unclear time in: {email['subject']}", "info")
        print("[SCHEDULE] Time unclear — follow-up question sent")
        save_processed_id(email["id"])
        mark_as_read(service, email["id"])
        return

    # Time is clear — proceed with normal scheduling
    slots_params = extract_slots_from_text(email["body"])
    slots = find_slots(attendees, slots_params["time_min"], slots_params["time_max"])
    print(f"[SCHEDULE] Found {len(slots)} slots")

    event_result = None
    if slots:
        event_result = create_event(
            summary=email.get("subject") or "Meeting via Mail Pilot",
            slot=slots[0],
            attendees=attendees,
            description=f"Scheduled via Mail Pilot.\n\nOriginal request:\n{email['body'][:500]}"
        )
        print(f"[SCHEDULE] Event: {event_result}")

    try:
        summary_resp = requests.post(
            "https://api.groq.com/openai/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {os.getenv('GROQ_API_KEY')}",
                "Content-Type": "application/json"
            },
            json={
                "model": "llama-3.3-70b-versatile",
                "messages": [
                    {
                        "role": "system",
                        "content": (
                            "Summarize this scheduling email in exactly one sentence. "
                            "Mention who wants to meet, when, and with whom if mentioned. "
                            "Be concise and specific. No bullet points."
                        )
                    },
                    {
                        "role": "user",
                        "content": email["body"][:500]
                    }
                ],
                "temperature": 0,
                "max_tokens": 60
            },
            timeout=10
        ).json()
        ai_summary = summary_resp["choices"][0]["message"]["content"].strip()
    except Exception:
        ai_summary = f"Scheduling request from {email['sender']}"

    if event_result and event_result.get("status") == "created":
        html_link = event_result.get("html_link", "")
        log_activity("Meeting scheduled", f"{ai_summary}|{html_link}", "success")
    elif event_result and event_result.get("status") == "duplicate":
        log_activity("Duplicate meeting blocked", ai_summary, "info")
    else:
        log_activity("Schedule request - no slots found", ai_summary, "error")

    reply = build_schedule_reply(slots, event_result, attendees)
    send_reply(service, email, reply)
    print("[SCHEDULE] Reply sent")


